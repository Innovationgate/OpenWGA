package de.innovationgate.utils;

/**
 * Tool interface for classes that provide a class loader for various purposes.
 * Can be used when a class loader should be passed on to a method, but the class loader
 * should be fetched at real execution time, not call time of the method. Therefor
 * the ClassLoaderProvider can be given as parameter which allows the method to fetch
 * the class loader at the moment it is really used.
 */
public interface ClassLoaderProvider {
    
    /**
     * Returns the current class loader
     */
    public ClassLoader getClassLoader();

}
