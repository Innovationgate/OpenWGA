package de.innovationgate.wgpublisher.filter;

import javax.servlet.Filter;

/**
 * Servlet filter implementing IndexProvidingServletFilter can enforce
 * their index in the filter chain to be executed before built-in OpenWGAFilters
 * 
 * builtin filters will use indexes from 100 - 899
 * custom filters will use indexes >= 900 related to their order in the config file
 */
public interface IndexProvidingServletFilter extends Filter {
    
    public int getIndex();

}
